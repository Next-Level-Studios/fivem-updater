def test_setup_wizard_can_bootstrap_brand_new_server(monkeypatch, tmp_path):
    monkeypatch.setenv("XDG_CONFIG_HOME", str(tmp_path / "xdg-config"))
    runtime = tmp_path / "runtime"
    data_path = tmp_path / "server-data"
    cfg_path = data_path / "server.cfg"
    text_answers = iter([
        str(runtime),
        "Dev Survival",
        "devsurvival",
        str(data_path),
        str(cfg_path),
        "40120",
        "30120",
        "0.0.0.0",
    ])
    updates = []
    starts = []

    monkeypatch.setattr("fivemanager.wizard.ask_select", lambda message, choices: "manager" if "What do you want" in message else "done")
    monkeypatch.setattr("fivemanager.wizard.ask_text", lambda *args, **kwargs: next(text_answers))
    monkeypatch.setattr("fivemanager.wizard.ask_path", lambda *args, **kwargs: next(text_answers))
    monkeypatch.setattr("fivemanager.wizard.ask_confirm", lambda *args, **kwargs: True)
    monkeypatch.setattr("fivemanager.wizard.ask_checkbox", lambda *args, **kwargs: [1])

    def fake_update(config, force=False):
        updates.append((config["runtime_dir"], force))
        runtime.mkdir(parents=True, exist_ok=True)
        (runtime / "alpine").mkdir(exist_ok=True)
        (runtime / "run.sh").write_text("#!/bin/sh\n")

    run_setup_wizard(update_callback=fake_update, start_callback=lambda config, sid: starts.append(sid))

    assert updates == [(str(runtime), True)]
    assert starts == [1]
    assert (runtime / "txData" / "devsurvival" / "default" / "config.json").is_file()
    assert (data_path / "resources").is_dir()
    assert cfg_path.is_file()


def test_start_server_builds_expected_tmux_command(monkeypatch, tmp_path):
    calls = []
    monkeypatch.setattr("fivemanager.tmux.session_exists", lambda name: False)
    monkeypatch.setattr(subprocess, "run", lambda args, **kwargs: calls.append(args) or type("R", (), {"returncode": 0})())
    server_data = tmp_path / "server-data"
    server_data.mkdir()
    runtime = tmp_path / "runtime"
    runtime.mkdir()
    (runtime / "run.sh").write_text("#!/bin/sh\n")
    (runtime / "run.sh").chmod(0o755)
    (server_data / "server.cfg").write_text("endpoint_add_tcp '0.0.0.0:30120'\n")
    server = {
        "id": 1,
        "name": "Main",
        "key": "main",
        "cfg_path": str(server_data / "server.cfg"),
        "txadmin_port": 40120,
        "fxserver_port": 30120,
        "interface": "0.0.0.0",
    }

    name = start_server(runtime, server)

    assert name == "fivemanager-1-main"
    cmd = calls[0]
    joined = " ".join(cmd)
    assert "TXHOST_DATA_PATH" in joined
    assert str(runtime / "txData" / "main") in joined
    assert "TXHOST_TXA_PORT" in joined
    assert str(runtime / "run.sh") in joined
    assert "+exec" not in joined
    assert str(server_data) not in joined
    assert "server.cfg" not in joined
