def test_start_server_requires_installed_runtime(monkeypatch, tmp_path):
    monkeypatch.setattr("fivemanager.tmux.session_exists", lambda name: False)
    server_data = tmp_path / "server-data"
    server_data.mkdir()
    cfg_path = server_data / "server.cfg"
    cfg_path.write_text("# config\n")
    server = {"id": 1, "name": "Main", "key": "main", "cfg_path": str(cfg_path), "txadmin_port": 40120, "fxserver_port": 30120}

    try:
        start_server(tmp_path / "runtime", server)
    except RuntimeError as exc:
        assert "missing" in str(exc)
        assert "update-runtime" in str(exc)
    else:
        raise AssertionError("start_server should fail when run.sh is missing")


